function mostrarMapa($mapa) {
    foreach ($mapa as $fila) {
        echo implode(' ', $fila) . "<br>";
    }
}

function marcarImpactosUrbanos($mapa, $impactos) {
    foreach ($impactos as $impacto) {
        list($y, $x) = $impacto;
        if (isset($mapa[$y][$x]) && $mapa[$y][$x] === 'A') {
            $mapa[$y][$x] = 'C';
        }
    }
    return $mapa;
}

function calcularPersonasAfectadas($mapa) {
    $personasPorKm2 = 5000;
    $personasAfectadas = 0;
    $colirio = 0.25;
    foreach ($mapa as $fila) {
        foreach ($fila as $celda) {
            if ($celda === 'C') {
                $personasAfectadas += $personasPorKm2;
            }
        }
    }
    return $personasAfectadas;
}

function calcularColirioNecesario($personasAfectadas) {
    $colirioPorPersona = 25; 
    return ($personasAfectadas * $colirioPorPersona) / 1000; 
}

function marcarTodosImpactos($mapa, $impactos) {
    foreach ($impactos as $impacto) {
        list($y, $x) = $impacto;
        if (isset($mapa[$y][$x])) {
            if ($mapa[$y][$x] === '0') {
                $mapa[$y][$x] = 'X';
            } elseif ($mapa[$y][$x] === '~') {
                $mapa[$y][$x] = 'S';
            }
        }
    }
    return $mapa;
}

function calcularDañosEconomicos($mapa) {
    $dañoUrbanoPorKm2 = 200000;
    $dañoNoUrbanoPorKm2 = 50000;
    $dañoUrbano = $dañoNoUrbano = 0;
    foreach ($mapa as $fila) {
        foreach ($fila as $celda) {
            if ($celda === 'C') {
                $dañoUrbano += $dañoUrbanoPorKm2;
            } elseif ($celda === 'X') {
                $dañoNoUrbano += $dañoNoUrbanoPorKm2;
            }
        }
    }
    return [$dañoUrbano + $dañoNoUrbano, $dañoUrbano, $dañoNoUrbano];
}

function calcularMar($mapa) {
    $marTotal = 0;
    $marAfectado = 0;
    foreach ($mapa as $fila) {
        foreach ($fila as $celda) {
            if ($celda === '~') {
                $marTotal++;
            } elseif ($celda === 'S') {
                $marAfectado++;
            }
        }
    }
    return [$marTotal, $marAfectado];
}

function calcularRecaudacion($marAfectado) {
    $pescadoPorKm2 = 2000; 
    $precioPorKg = 5; 
    return $marAfectado * $pescadoPorKm2 * 1000 * $precioPorKg; 
}

